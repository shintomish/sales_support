/**
 * SES台帳データ投入シーダー（2026-04-16版）
 * 実行前に既存SESデータを全削除する。
 */
class SesLedgerSeeder extends Seeder
{
    private int $tenantId = 2;
    private int $userId   = 2;

    public function run(): void
    {
        DB::transaction(function () {
            $this->deleteExisting();
            foreach ($this->getData() as $row) {
                $this->processRow($row);
            }
        });

        $this->command->info('SES台帳データ投入完了: ' . count($this->getData()) . '件');
    }

    // ── 既存データ削除 ──────────────────────────────────────

    private function deleteExisting(): void
    {
        // SESデールIDを取得
        $sesIds = Deal::withTrashed()
            ->where('tenant_id', $this->tenantId)
            ->where('deal_type', 'ses')
            ->pluck('id');

        WorkRecord::whereIn('deal_id', $sesIds)->delete();
        DealAssignee::whereIn('deal_id', $sesIds)->delete();
        SesContract::whereIn('deal_id', $sesIds)->forceDelete();
        Deal::whereIn('id', $sesIds)->forceDelete();

        // engineersテーブル削除
        $engIds = Engineer::withTrashed()->where('tenant_id', $this->tenantId)->pluck('id');
        EngineerSkill::whereIn('engineer_id', $engIds)->delete();
        EngineerProfile::whereIn('engineer_id', $engIds)->delete();
        Engineer::whereIn('id', $engIds)->forceDelete();

        $this->command->info("削除完了: deals={$sesIds->count()}, engineers={$engIds->count()}");
    }

    // ── 1行処理 ────────────────────────────────────────────

    private function processRow(array $r): void
    {
        // 顧客
        $customer = Customer::firstOrCreate(
            ['tenant_id' => $this->tenantId, 'company_name' => $r['customer']],
            ['tenant_id' => $this->tenantId, 'company_name' => $r['customer']]
        );

        // Contact（技術者）
        $contactKey = !empty($r['email'])
            ? ['tenant_id' => $this->tenantId, 'email' => $r['email'], 'name' => $r['name']]
            : ['tenant_id' => $this->tenantId, 'name' => $r['name'], 'customer_id' => $customer->id];

        $contact = Contact::updateOrCreate($contactKey, [
            'tenant_id'   => $this->tenantId,
            'customer_id' => $customer->id,
            'name'        => $r['name'],
            'email'       => $r['email'] ?? null,
            'phone'       => $r['phone'] ?? null,
        ]);

        // Deal
        $dealKey = $r['project_number'] > 0
            ? ['tenant_id' => $this->tenantId, 'project_number' => $r['project_number']]
            : ['tenant_id' => $this->tenantId, 'title' => $r['project_name'], 'customer_id' => $customer->id];

        $status = match (true) {
            str_contains($r['change_type'] ?? '', '退場') => '期限切れ',
            str_contains($r['change_type'] ?? '', '新規') => '新規',
            default                                        => '稼働中',
        };

        $deal = Deal::updateOrCreate($dealKey, [
            'tenant_id'           => $this->tenantId,
            'customer_id'         => $customer->id,
            'contact_id'          => $contact->id,
            'title'               => $r['project_name'] ?: "{$r['name']} / {$r['customer']}",
            'deal_type'           => 'ses',
            'project_number'      => $r['project_number'] ?: null,
            'end_client'          => $r['end_client'] ?? null,
            'nearest_station'     => $r['station'] ?? null,
            'change_type'         => $r['change_type'] ?? null,
            'affiliation'         => $r['affiliation'] ?? null,
            'affiliation_contact' => $r['aff_contact'] ?? null,
            'sales_person'        => $r['sales_person'] ?? null,
            'invoice_number'      => $r['invoice'] ?? null,
            'client_contact'      => $r['client_contact'] ?? null,
            'client_mobile'       => $r['client_mobile'] ?? null,
            'client_phone'        => $r['client_phone'] ?? null,
            'client_fax'          => $r['client_fax'] ?? null,
            'notes'               => $r['notes'] ?? null,
            'status'              => $status,
            'user_id'             => $this->userId,
        ]);

        // SesContract
        SesContract::updateOrCreate(['deal_id' => $deal->id], [
            'tenant_id'                   => $this->tenantId,
            'deal_id'                     => $deal->id,
            'income_amount'               => $r['income'] ?? null,
            'billing_plus_22'             => $r['bill22'] ?? null,
            'billing_plus_29'             => $r['bill29'] ?? null,
            'sales_support_payee'         => $r['sup_payee'] ?? null,
            'sales_support_fee'           => $r['sup_fee'] ?? null,
            'adjustment_amount'           => $r['adj'] ?? null,
            'profit'                      => $r['profit'] ?? null,
            'profit_rate_29'              => $r['profit29'] ?? null,
            'client_deduction_unit_price' => $r['c_ded_price'] ?? null,
            'client_deduction_hours'      => $r['c_ded_hrs'] ?? null,
            'client_overtime_unit_price'  => $r['c_ovt_price'] ?? null,
            'client_overtime_hours'       => $r['c_ovt_hrs'] ?? null,
            'settlement_unit_minutes'     => $r['settle_min'] ?? null,
            'payment_site'                => $r['pay_site'] ?? null,
            'vendor_deduction_unit_price' => $r['v_ded_price'] ?? null,
            'vendor_deduction_hours'      => $r['v_ded_hrs'] ?? null,
            'vendor_overtime_unit_price'  => $r['v_ovt_price'] ?? null,
            'vendor_overtime_hours'       => $r['v_ovt_hrs'] ?? null,
            'vendor_payment_site'         => $r['v_pay_site'] ?? null,
            'contract_start'              => $r['cs'] ?? null,
            'contract_period_start'       => $r['cps'] ?? null,
            'contract_period_end'         => $r['cpe'] ?? null,
            'affiliation_period_end'      => $r['ape'] ?? null,
        ]);
    }

